#---------- shovel (<<) -- add element to the end

famous_cats = ["lil' bub", "grumpy cat", "Maru"]

famous_cats << "nala cat" #=> famous_cats = ["lil' bub", "grumpy cat", "Maru", "nala cat"]

#---------- .push -- add element to the end

famous_cats = ["lil' bub", "grumpy cat", "Maru"]

famous_cats.push("nala cat") #=> famous_cats = ["lil' bub", "grumpy cat", "Maru", "nala cat"]

#---------- .unshift add element to the front 

famous_cats = ["lil' bub", "grumpy cat", "Maru"]

famous_cats.unshift("nala cat") #=> famous_cats = ["nala cat", "lil' bub", "grumpy cat", "Maru"]